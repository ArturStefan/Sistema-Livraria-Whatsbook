    public void sugestoescliente(){
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/biblioteca", "root", "");
            Statement stm = con.createStatement();
            ResultSet res = stm.executeQuery("SELECT * FROM sugestao");
            sugestao="";
            while(res.next()) {
                
                sugestao+="\n"+res.getString("id");
                sugestao+="\n"+res.getString("mensagem");
                sugestao+="\n----------------------------------------------------------------------------------------------------------------------------------------";
            }
            jTextArea1.setText(sugestao);
            
            //desabilitar botão de excluir ate selecioar uma sugestão do cliente
            desabiliarbotao(true);

        } catch(ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null,"Erro: "+e.getMessage(),"Erro",JOptionPane.ERROR_MESSAGE);
        }
    }
    
