    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().equals(""))
        {
        JOptionPane.showMessageDialog(null, "Preencha o Campo ID para excluir!");
        }
        else
        {
        
        id = jTextField1.getText();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/biblioteca", "root", "");
            Statement stm = con.createStatement();
            if (stm.executeUpdate("DELETE FROM sugestao WHERE id='"+id+"'")!=0) 
            {
                JOptionPane.showMessageDialog(null,"Removido com sucesso!!!!","Sucesso", JOptionPane.INFORMATION_MESSAGE);
                
                 jTextField1.setText("");
                 
                //desabilitar botão de excluir ate selecioar uma sugestão do cliente
                desabiliarbotao(false);
                
                //atualizar lista de sugestões apos excluir
                sugestoescliente();
           }
        }
        catch(ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null,"Erro: "+e.getMessage(),"Erro",JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(null, "Não existe nenhuma sugestão com este ID!");
        }
        }
       
    }//GEN-LAST:event_jButton2ActionPerformed

