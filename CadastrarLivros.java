    public void cadastrarimagem(){
        
        Conexao con = new Conexao();
        con.conecta();
        String caminho = jLabel14.getIcon().toString();
        //ntombo = Integer.parseInt(jTextField10.getText());
        InputStream fis;
        File file = new File(caminho);
        try {
            fis = new FileInputStream(file);
            PreparedStatement ps = con.conexao.prepareStatement("INSERT into cadastro_livros_fotos (cod,foto) VALUES (?,?)");
            int cod = ntombo;
            ps.setInt(1, cod);
            ps.setBinaryStream(2, fis, (int)file.length());
            ps.executeUpdate();      
            ps.close();
            
            fis.close();
            
            //limpar o jlabel apos salvar imagem
            jLabel14.setIcon(null);
            
        } catch (HeadlessException | IOException | SQLException ex) {
        }
    }
    
    
