    void tombolivro(){
        
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/biblioteca", "root", "");
            Statement stm = con.createStatement();
            ResultSet res = stm.executeQuery("select * from cadastro_livros where tombo = (SELECT MAX(tombo) FROM cadastro_livros)");
         while (res.next())
            {
                ntombo = res.getInt("tombo");
            }
            ntombo+=1;
            jTextField10.setText(""+ntombo);

            con.close();
        }
        catch (ClassNotFoundException | SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Erro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
    
